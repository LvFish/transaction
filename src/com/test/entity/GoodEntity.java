package com.test.entity;

public class GoodEntity {
	private int id;
	private String name;	
	private String url;	
	private String price;	
	private String detail;	
	private String publishedTime;
	private String publishedStatus;
	private int publishedBy;
	private String publishedByName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getPublishedTime() {
		return publishedTime;
	}
	public void setPublishedTime(String publishedTime) {
		this.publishedTime = publishedTime;
	}
	public String getPublishedStatus() {
		return publishedStatus;
	}
	public void setPublishedStatus(String publishedStatus) {
		this.publishedStatus = publishedStatus;
	}
	public int getPublishedBy() {
		return publishedBy;
	}
	public void setPublishedBy(int publishedBy) {
		this.publishedBy = publishedBy;
	}
	public String getPublishedByName() {
		return publishedByName;
	}
	public void setPublishedByName(String publishedByName) {
		this.publishedByName = publishedByName;
	}
	

}
